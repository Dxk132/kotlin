package `1_tipos_de_variables`

fun main() {
    //val=inmutable var=mutable
    //variable inmutamble de tipo entrero
    val edad: Int
    edad = 48
    //variable inmutable de tipo decimal
    val sueldos: Float
    sueldos = 1200.55f
    //variable inmutable de tipo decimal
    val total: Double
    total = 70000.24

    //variable inmutable de tipo alfanumerico
    val titulo: String
    titulo = "sistema de ventanas"

    //variable mutable de tipo entero
    var mes: Int
    mes = 1
    //algunas lineas de codigo
    mes = 2

    //char almacena un solo caracter (usar con ')
    var continua: Char = 's'

    //variable de tipo booleano, true o false
    var fin: Boolean = false

}